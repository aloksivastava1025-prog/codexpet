export const HATS: Record<string, string> = {
  none: '',
  crown: '   \\^^^/    ',
  tophat: '   [___]    ',
  propeller: '    -+-     ',
  halo: '   (   )    ',
  wizard: '    /^\\     ',
  beanie: '   (___)    ',
  tinyduck: '    ,>      ',
};

export const SPECIES = Object.keys(BODIES);

export function renderSprite(comp: { species: string; eye: string; hat: string }, frame: number = 0): string {
  const frames = BODIES[comp.species];
  if (!frames) return '';
  const f = frame % frames.length;
  const lines = [...frames[f]].map(l => l.replace(/E/g, comp.eye));
  if (comp.hat !== 'none' && !lines[0].trim()) {
    lines[0] = HATS[comp.hat] || '';
  }
  return lines.join('\n');
}
